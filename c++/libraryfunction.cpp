#include<iostream>
#include<cmath>
using namespace std;
int main(){
//sqet(), cbrt(),min(), max(), pow()
    int a=5,b=10;
    cout<<"the maximum value is "<<max(a,b);
    cout<<"\nthe minimum value is "<<min(a,b);
    cout<<"\nthe square root of a is "<<sqrt(a);
    cout<<"\nthe cube root of b is "<<cbrt(b);
    cout<<"\nthe value of a to the power b is "<<pow(a,b);                   
}